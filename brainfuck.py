#coding:utf-8

convert_list = {
    ">" :"バカ" ,
    "<" :"ボケ",
    "+" :"死ね",
    "-" :"殺す",
    "." :"カス",
    "," :"ゴミ",
    "[" :"クズ",
    "]" :"クソ",
}

def conversion(code):
    for i,j in convert_list.iteritems():
        code = code.replace(i,j)
    return code

